# ── Strict Scoring ────────────────────────────────────────────────────────────

def score_reply(qid, section, bot_reply, expected):
    """
    Day 5 — صارم
    2 = مستوى الجواب المتوقع أو أفضل
    1 = مقبول لكن الجواب المتوقع أفضل (أطول/أبرد/أقل حسمًا)
    0 = خطأ
    """
    if bot_reply.startswith("ERROR"):
        return 0, "خطأ تقني", "الجواب المتوقع"

    # ── فشل: أعاد الترحيب ────────────────────────────────────────────────────
    if bot_reply.strip() in REGREETING:
        if section in ("تبديل متكرر",):
            return 0, "❌ أعاد الترحيب في وسط المحادثة", "الجواب المتوقع"

    # ── فشل: باع بدون مناسبة في قسم التبديل ──────────────────────────────────
    if section == "تبديل متكرر":
        if any(t in bot_reply for t in SELL_TRAPS):
            return 0, "❌ عرض منتج بدل تأكيد التغيير", "الجواب المتوقع"
        confirm_kws = [
            "تمام","تم","أكيد","وصلني","وصلت","بدلناها","رجعناها",
            "صارت","حدّثت","حذفت","ألغيت","ثبّت","ثبتت","ناقص",
            "بدون","حار","عادي","استلام","توصيل","كاش",
        ]
        if not any(k in bot_reply for k in confirm_kws):
            return 0, "❌ ما أكد التغيير", "الجواب المتوقع"

    # ── فشل: قسم الأخطاء الإملائية — ما فهم ────────────────────────────────
    if section == "أخطاء إملائية":
        understand_kws = ["أكيد","تمام","تم","وصلني","وصلت","بركر","زينگر","دجاج",
                          "بطاطا","كولا","بيبسي","حار","عادي","سفري","دبل","منيو",
                          "توصيل","كاش","محمد","علي","المنصور","الكرادة","هلا",
                          "بخير","شلون","نوصل","يوصل","عرض","حالياً","مستمر","نعم","إي"]
        if not any(k in bot_reply for k in understand_kws):
            return 0, "❌ ما فهم الطلب رغم الخطأ الإملائي", "الجواب المتوقع"

    # ── فشل: رسائل طويلة — أعاد سؤالًا عن معلومة موجودة ─────────────────────
    if section == "رسائل طويلة":
        confirm_kws = ["تم","سجلت","ثبّتت","أكيد","تمام","وصلت",
                       "وصلني","أرشحلك","ذكّرني","كللي",
                       "آسف","ما عندنا","مو متوفر","غير متوفر","نعم","إي"]
        if not any(k in bot_reply for k in confirm_kws):
            return 0, "❌ ما أكد استلام المعلومات", "الجواب المتوقع"

    # ── فشل: قسم غامض مختصر — رد عشوائي أو بيع ─────────────────────────────
    if section == "غامض مختصر":
        fail_signs = [
            "شنو تحب تطلب؟ 🌷",
            "هلا بيك 🌷 شلون أكدر أخدمك؟",
        ]
        if bot_reply.strip() in fail_signs:
            return 0, "❌ تجاهل الغموض وبدأ من الأول", "الجواب المتوقع"

    # ── الرد صحيح — نقيّم الجودة بصرامة ────────────────────────────────────
    score  = _quality_score(bot_reply, expected, section)
    winner = _winner(bot_reply, expected, score, section)
    note   = _note(score, bot_reply, expected, section)
    return score, note, winner


def _quality_score(bot_reply, expected, section):
    """2 = جيد، 1 = مقبول لكن يمكن أفضل"""
    bot_len = len(bot_reply)
    exp_len = len(expected)

    # نبرة رسمية/باردة
    if any(p in bot_reply for p in COLD_PHRASES):
        return 1

    # التبديل المتكرر: المتوقع قصير جدًا (<50 حرف) — البوت لازم يكون مثله
    if section == "تبديل متكرر":
        return 2 if bot_len <= 80 else 1

    # الأخطاء الإملائية: ردود قصيرة مطلوبة
    if section == "أخطاء إملائية":
        return 2 if bot_len <= 60 else 1

    # باقي الأقسام: إذا البوت أطول بـ 40%+ → مقبول لكن الجواب المتوقع أفضل
    if exp_len > 20 and bot_len > exp_len * 1.4:
        return 1

    return 2


def _winner(bot_reply, expected, score, section):
    if score == 0:
        return "الجواب المتوقع"

    bot_len = len(bot_reply)
    exp_len = len(expected)

    # نبرة باردة رسمية → الجواب المتوقع أفضل
    if any(p in bot_reply for p in COLD_PHRASES):
        return "الجواب المتوقع"

    # أعاد الترحيب (في التبديل المتكرر بالأخص) → الجواب المتوقع أفضل
    if any(g in bot_reply for g in REGREETING):
        return "الجواب المتوقع"

    # التبديل/الإملائية: البوت أطول من المتوقع بـ 20% → الجواب المتوقع أفضل
    if section in ("تبديل متكرر", "أخطاء إملائية") and bot_len > exp_len * 1.2:
        return "الجواب المتوقع"

    # أي قسم: البوت أطول بـ 30%+ → الجواب المتوقع أفضل
    if exp_len > 20 and bot_len > exp_len * 1.3:
        return "الجواب المتوقع"

    # البوت أقصر بشكل ملحوظ → البوت أفضل
    if bot_len <= exp_len * 0.85:
        return "البوت"

    return "متقارب"


def _note(score, bot_reply, expected, section):
    if score == 0:
        return "❌ فشل"
    if score == 1:
        bot_len, exp_len = len(bot_reply), len(expected)
        if any(p in bot_reply for p in COLD_PHRASES):
            return "⚠️ نبرة باردة"
        if section in ("تبديل متكرر", "أخطاء إملائية") and bot_len > 80:
            return "⚠️ طويل للقسم"
        if exp_len > 20 and bot_len > exp_len * 1.4:
            return f"⚠️ طويل ({bot_len}>{exp_len})"
        return "⚠️ مقبول لكن يمكن أفضل"
    return "✅ جاهز"


# ── Analysis ──────────────────────────────────────────────────────────────────

def analyze_patterns(results):
    """
    بعد التشغيل:
    - أكثر 20 حالة الجواب المتوقع كان أوضح
    - أبرز الأنماط اللي لازم البوت يقلدها
    """
    exp_better = [r for r in results if r[7] == "الجواب المتوقع"]
    exp_better_sorted = sorted(exp_better,
                               key=lambda r: len(r[3]) - len(r[4]),  # bot_len - exp_len
                               reverse=True)

    lines = []
    lines.append("\n" + "="*70)
    lines.append("📊 تحليل Day 5 — أين الجواب المتوقع أفضل؟")
    lines.append("="*70)

    lines.append(f"\n🔴 أكثر 20 حالة الجواب المتوقع كان أوضح:\n")
    top20 = exp_better_sorted[:20]
    for i, r in enumerate(top20, 1):
        qid, section, question, bot_reply, expected, score, note, _ = r
        lines.append(f"  {i:2}. [{qid}] {section} — {question[:35]}")
        lines.append(f"       البوت     : {bot_reply[:80]}")
        lines.append(f"       المتوقع   : {expected[:80]}")
        lines.append(f"       الفرق     : {note}\n")

    # ── Pattern analysis ─────────────────────────────────────────────────────
    lines.append("\n" + "─"*70)
    lines.append("🔍 أبرز الأنماط اللي لازم البوت يقلدها من الأجوبة المتوقعة:\n")

    patterns = {
        "تأكيد سريع (تم/تمام/أكيد) بدون شرح زيادة": 0,
        "لا يعيد معلومات ذكرها العميل": 0,
        "يربط الشرط بالحل مباشرة (إذا X → Y)": 0,
        "يطلب فقط المعلومة الناقصة": 0,
        "ردود ≤ 50 حرف في التبديل/الإملائية": 0,
        "لا يبدأ بـ (هلا بيك) في وسط المحادثة": 0,
        "لهجة عراقية خفيفة (كللي / إذا / هسه)": 0,
    }

    for r in exp_better:
        qid, section, question, bot_reply, expected, score, note, _ = r
        if len(bot_reply) > len(expected) * 1.3:
            patterns["تأكيد سريع (تم/تمام/أكيد) بدون شرح زيادة"] += 1
        if section == "رسائل طويلة" and len(bot_reply) > 100:
            patterns["لا يعيد معلومات ذكرها العميل"] += 1
        if "إذا" in expected and "إذا" not in bot_reply:
            patterns["يربط الشرط بالحل مباشرة (إذا X → Y)"] += 1
        if "كللي" in expected and "كللي" not in bot_reply:
            patterns["يطلب فقط المعلومة الناقصة"] += 1
        if section in ("تبديل متكرر", "أخطاء إملائية") and len(bot_reply) > 50:
            patterns["ردود ≤ 50 حرف في التبديل/الإملائية"] += 1
        if any(g in bot_reply for g in REGREETING):
            patterns["لا يبدأ بـ (هلا بيك) في وسط المحادثة"] += 1
        if "كللي" in expected or "هسه" in expected:
            patterns["لهجة عراقية خفيفة (كللي / إذا / هسه)"] += 1

    for pattern, count in sorted(patterns.items(), key=lambda x: -x[1]):
        if count > 0:
            lines.append(f"  • {pattern} ({count} حالة)")

    # ── Recommendations ───────────────────────────────────────────────────────
    lines.append("\n" + "─"*70)
    lines.append("💡 توصيات لتقريب أسلوب البوت من مستوى الأجوبة المتوقعة:\n")
    lines.append("  1. في التبديل المتكرر: الرد المثالي = تمام 🌷 + تأكيد بكلمة واحدة (حار/عادي/توصيل...)")
    lines.append("  2. في الأخطاء الإملائية: افهم، صحح، وأكد — لا تشرح")
    lines.append("  3. في الرسائل الطويلة: اعكس كل المعلومات في جملة واحدة مرتبة")
    lines.append("  4. في النية المختلطة: جاوب الجزأين دفعة وحدة مو كل جزء لحاله")
    lines.append("  5. في الغامض: لا تعيد الترحيب — اطلب التوضيح أو افترض المعنى الأقرب")
    lines.append("  6. استخدم (كللي / هسه / إذا / وإذا لا) أكثر — هذه تدفع الحوار للأمام")

    return "\n".join(lines)


