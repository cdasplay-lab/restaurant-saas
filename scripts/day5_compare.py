def get_token():
    data = json.dumps({"email": "admin@restaurant.com", "password": "admin123"}).encode()
    req  = urllib.request.Request(
        f"{BASE}/api/auth/login", data=data,
        headers={"Content-Type": "application/json"}, method="POST")
    with urllib.request.urlopen(req, timeout=10) as r:
        return json.loads(r.read())["token"]


def simulate(msg, token):
    """Send single message, return bot reply."""
    data = json.dumps({"messages": [msg], "scenario": "default"}).encode()
    req  = urllib.request.Request(
        f"{BASE}/api/bot/simulate", data=data,
        headers={"Content-Type": "application/json",
                 "Authorization": f"Bearer {token}"},
        method="POST")
    try:
        with urllib.request.urlopen(req, timeout=90) as r:
            body    = json.loads(r.read())
            results = body.get("results", [])
            return results[-1].get("bot", "") if results else ""
    except Exception as e:
        return f"ERROR:{e}"


