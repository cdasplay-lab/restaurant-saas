def run(token=None):
    if token is None:
        token = U.get_token()

    tests = build_tests(token)

    passed, total, pct = U.run_suite(
        "Core Behavior Tests (Layer 1)", tests, token, delay=DELAY
    )

    # Extra: verify BANNED_PHRASES not in greeting response
    reply = U.simulate("هلا، شنو عندكم؟", token)
    clean = U.none_of(reply, BANNED_PHRASES)
    status = f"{U.GRN}✅{U.RST}" if clean else f"{U.RED}❌{U.RST}"
    print(f"\n  {status} BANNED phrases check: {'نظيف' if clean else 'وجد عبارة ممنوعة!'}")
    if not clean:
        for b in BANNED_PHRASES:
            if b in reply:
                print(f"       ← وجد: \"{b}\"")

    return passed, total, pct


if __name__ == "__main__":
    run()
