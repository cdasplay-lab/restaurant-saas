# ── Runner ────────────────────────────────────────────────────────────────────
BOLD = "\033[1m"; RED = "\033[31m"; GRN = "\033[32m"
YLW = "\033[33m"; RST = "\033[0m"

SECTION_TOTAL = {
    "شكوى": 40, "غضب": 25, "handoff": 25,
    "متابعة": 25, "تعويض": 20, "tricky": 15,
}


def run():
    print(f"\n{BOLD}═══════════════════════════════════════════════════════")
    print(f"  Day 4 — Complaints / Handoff / After-Order Support")
    print(f"  150 حالة")
    print(f"═══════════════════════════════════════════════════════{RST}\n")

    results      = []
    section_data = {}
    sell_count   = 0
    defend_count = 0

    for (qid, section, msg, keywords) in TESTS:
        reply  = simulate(msg)
        score  = score_reply(reply, keywords)
        sell   = is_selling(reply)
        defend = is_defending(reply)

        # Selling during complaint = automatic fail (شكوى and غضب only)
        if sell and section in ("شكوى", "غضب"):
            score = 0
            sell_count += 1
        elif sell:
            sell_count += 1  # count but don't penalize (other sections)

        if defend:
            defend_count += 1

        col   = GRN if score == 2 else (YLW if score == 1 else RED)
        flags = (" ⚠️SELL" if sell else "") + (" ⚠️DEFEND" if defend else "")
        short = reply[:65] + ("…" if len(reply) > 65 else "")
        dmsg  = (msg if isinstance(msg, str) else msg[-1])[:30]
        print(f"  [{col}{score}{RST}] [{qid:3}] [{section}] {dmsg:<32} → {short}{flags}")

        results.append((qid, section, score, sell, defend, reply, msg))
        if section not in section_data:
            section_data[section] = []
        section_data[section].append(score)
        time.sleep(DELAY)

    # ── Summary ───────────────────────────────────────────────────────────────
    total  = len(results)
    passed = sum(1 for _, _, s, *_ in results if s > 0)
    s2     = sum(1 for _, _, s, *_ in results if s == 2)
    s1     = sum(1 for _, _, s, *_ in results if s == 1)
    failed = sum(1 for _, _, s, *_ in results if s == 0)
    pct    = passed * 100 // total

    print(f"\n{BOLD}═══════════════════════════════════════════════════════")
    print(f"  RESULTS: {s2}/150 جاهز  {s1}/150 مقبول  {failed}/150 خطأ")
    print(f"  PASS rate: {passed}/150 ({pct}%)")
    if sell_count:
        print(f"  ⚠️  Sell during complaint: {sell_count} replies")
    if defend_count:
        print(f"  ⚠️  Defensive replies:     {defend_count} replies")
    print(f"═══════════════════════════════════════════════════════{RST}\n")

    # ── Failures ──────────────────────────────────────────────────────────────
    fail_list = [(qid, sec, reply, msg)
                 for (qid, sec, score, *_, reply, msg) in results if score == 0]
    if fail_list:
        print(f"{BOLD}{RED}FAILURES (score=0):{RST}")
        for (qid, sec, reply, msg) in fail_list:
            dmsg = msg if isinstance(msg, str) else msg[-1]
            print(f"  [{qid:3}] [{sec}] {dmsg}")
            print(f"        رد:  {reply[:80]}")

    # ── Category breakdown ────────────────────────────────────────────────────
    SEC_ORDER = ["شكوى","غضب","handoff","متابعة","تعويض","tricky"]
    print(f"\n{BOLD}Category Breakdown:{RST}")
    for sec in SEC_ORDER:
        if sec not in section_data: continue
        scores  = section_data[sec]
        ok      = sum(1 for s in scores if s > 0)
        bad     = sum(1 for s in scores if s == 0)
        total_s = SECTION_TOTAL[sec]
        bar_w   = int(ok / total_s * 40)
        bar     = "█" * bar_w + "░" * (40 - bar_w)
        col     = GRN if bad == 0 else (YLW if bad <= 2 else RED)
        print(f"  {col}{sec:<8}  {ok:2}✓  {bad:2}✗  [{bar}]{RST}")

    col = GRN if pct >= 95 else (YLW if pct >= 85 else RED)
    print(f"\n{col}{BOLD}Score: {pct}%{RST}")


if __name__ == "__main__":
    run()
