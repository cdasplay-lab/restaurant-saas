def setup_restaurant(rid, cfg):
    print(f"  ⚙️  إعداد {rid} — {cfg['restaurant_name']} ...", end=" ", flush=True)

    # Try login first (restaurant may already exist from a prior run)
    token = login_restaurant(cfg)
    if token:
        print(f"✅ جاهز (already_exists)", flush=True)
        # Still update products/settings below
    else:
        # Register — retry once if rate-limited
        reg_result = register_restaurant(cfg)
        if "429" in str(reg_result) or reg_result.startswith("error:429"):
            print(f"\n    ⏳ rate limit — انتظر 65 ثانية...", flush=True)
            time.sleep(65)
            reg_result = register_restaurant(cfg)
        time.sleep(1)
        token = login_restaurant(cfg)
        if not token:
            print("❌ فشل تسجيل الدخول")
            return None

    # Create products (ignore duplicates)
    products_resp, _ = _req("GET", "/api/products", token=token)
    existing_names = {p["name"] for p in (products_resp if isinstance(products_resp, list) else [])}

    for p in cfg["products"]:
        if p["name"] not in existing_names:
            _req("POST", "/api/products",
                 {"name": p["name"], "price": p["price"],
                  "category": p["category"], "description": p.get("description",""),
                  "available": True, "variants": [], "gallery_images": []},
                 token=token)
            time.sleep(0.3)

    # Update settings
    s = cfg["settings"].copy()
    _req("PUT", "/api/settings", s, token=token)

    return token


