    all_msg_scores = [s for r in scenario_results for s, _, _, _, _ in r["scores"]]
    good_msg = sum(1 for s in all_msg_scores if s == 2)
    total_msg = len(all_msg_scores)
    pct_msg = int(good_msg / total_msg * 100) if total_msg else 0

