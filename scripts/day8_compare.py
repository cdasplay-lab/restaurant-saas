TARGET_PCTS = {"R1": 95, "R2": 93, "R3": 95, "R4": 90, "R5": 93}

def main():
    # --focus R5 R3   → only test those restaurants (+ always runs iso + dynamic)
    focus = []
    args = sys.argv[1:]
    if "--focus" in args:
        idx = args.index("--focus")
        for a in args[idx+1:]:
            if a.startswith("R") and len(a) <= 3:
                focus.append(a)

    print(f"\n{'='*W}")
    print("  Day 8 — Multi-Restaurant Launch Simulation")
    if focus:
        print(f"  [FOCUS: {', '.join(focus)}]")
    print(f"  {datetime.datetime.now().strftime('%Y-%m-%d %H:%M')}")
    print(f"{'='*W}")

    # ── Step 1: Setup ────────────────────────────────────────────────────────
    print("\n⚙️  إعداد المطاعم...")
    tokens = {}
    for rid, cfg in RESTAURANT_CONFIGS.items():
        token = setup_restaurant(rid, cfg)
        if token:
            tokens[rid] = token
        time.sleep(13)  # 13s gap → max 4 new registrations/min, well under limit

    missing = [rid for rid in RESTAURANT_CONFIGS if rid not in tokens]
    if missing:
        print(f"\n❌ فشل إعداد: {missing}")
        sys.exit(1)

    print(f"\n✅ جميع المطاعم جاهزة — نبدأ الاختبارات\n")

    summary = {"restaurants": {}, "isolation": {}, "dynamic": {}, "overall_pct": 0}

    # ── Step 2: Per-Restaurant Tests ─────────────────────────────────────────
    all_pcts = []
    rids_to_test = focus if focus else list(RESTAURANT_CONFIGS.keys())
    for rid, cfg in {r: RESTAURANT_CONFIGS[r] for r in rids_to_test if r in RESTAURANT_CONFIGS}.items():
        token = tokens[rid]
        rname = cfg["restaurant_name"]

        print(f"\n{'═'*W}")
        print(f"  🏪 {rid} — {rname}")
        print(f"{'═'*W}")

        # Data tests
        print(f"\n  📊 Data Tests ({len(DATA_TESTS[rid])} اختبار)...")
        data_results = run_data_tests(rid, token, DATA_TESTS[rid])
        good_d = sum(1 for r in data_results if r["score"] == 2)
        print(f"  → {good_d}/{len(data_results)} صح")

        # Scenario tests
        scen_list = SCENARIOS.get(rid, [])
        print(f"\n  🧪 Scenario Tests ({len(scen_list)} سيناريو)...")
        scenario_results = []
        for scen in scen_list:
            result = run_scenario(scen, token)
            icon = "✅" if result["passed"] else "⚠️"
            print(f"  {icon} {result['id']} {result['name'][:35]} "
                  f"({result['n_good']}/{result['n_total']} رسائل)")
            scenario_results.append(result)
            time.sleep(INTER)

        # Restaurant report
        pct, ready = print_restaurant_report(
            rid, cfg, data_results, scenario_results, TARGET_PCTS[rid])
        summary["restaurants"][rid] = {"name": rname, "pct": pct, "ready": ready}
        all_pcts.append(pct)

    # ── Step 3: Isolation Tests ───────────────────────────────────────────────
    print(f"\n\n{'═'*W}")
    print("  🔒 Tenant Isolation Tests")
    print(f"{'═'*W}")
    iso_results = run_isolation_tests(tokens)
    print_isolation_report(iso_results)
    iso_passed = sum(1 for r in iso_results if r["passed"])
    summary["isolation"] = {"passed": iso_passed, "total": len(iso_results)}

    # ── Step 4: Dynamic Tests ─────────────────────────────────────────────────
    print(f"\n{'═'*W}")
    print("  ⚡ Dynamic Data Tests")
    print(f"{'═'*W}")
    dyn_results = run_dynamic_tests(tokens)
    print_dynamic_report(dyn_results)
    dyn_passed = sum(1 for r in dyn_results if r["passed"])
    summary["dynamic"] = {"passed": dyn_passed, "total": len(dyn_results)}

    # ── Final Report ──────────────────────────────────────────────────────────
    overall = int(sum(all_pcts) / len(all_pcts)) if all_pcts else 0
    summary["overall_pct"] = overall

    all_targets_met = all(
        summary["restaurants"].get(rid, {}).get("ready", False) for rid in rids_to_test)
    iso_clean = iso_passed == len(iso_results)
    dyn_clean = dyn_passed == len(dyn_results)
    launch_ready = all_targets_met and iso_clean

    summary["launch_ready"] = "🟢 جاهز للإطلاق" if launch_ready else "🔴 يحتاج إصلاح"

    print(f"\n{'='*W}")
    print("  النتيجة النهائية — Day 8")
    print(f"{'='*W}")
    for rid in RESTAURANT_CONFIGS:
        if rid in summary["restaurants"]:
            d = summary["restaurants"][rid]
            icon = "✅" if d["ready"] else "❌"
            print(f"  {icon} {rid} — {d['name']}: {d['pct']}%"
                  f"  (هدف {TARGET_PCTS[rid]}%)")
        else:
            print(f"  ⏭️  {rid} — (skipped)")
    print(f"\n  🔒 Isolation: {iso_passed}/{len(iso_results)} {'✅' if iso_clean else '❌'}")
    print(f"  ⚡ Dynamic:   {dyn_passed}/{len(dyn_results)} {'✅' if dyn_clean else '❌'}")
    print(f"\n  المتوسط الإجمالي: {overall}%")
    print(f"  {'🟢 جاهز للإطلاق الحقيقي' if launch_ready else '🔴 يحتاج إصلاح قبل الإطلاق'}")
    print(f"{'='*W}\n")

    save_report(summary)


if __name__ == "__main__":
    main()
