def run_scenario(scenario, token):
    thread  = scenario["thread"]
    user_msgs  = [m for m, _ in thread]
    bot_replies = []
    scores = []

    for i, (user_msg, expected) in enumerate(thread):
        window    = [m for m, _ in thread[:i+1]]
        bot_reply = simulate_thread(window, token)
        bot_replies.append(bot_reply)
        score, note = score_message(bot_reply, expected)
        scores.append((score, note, user_msg, expected, bot_reply))
        time.sleep(DELAY)

    violations = check_coherence(scenario, user_msgs, bot_replies)
    n_good = sum(1 for s, _, _, _, _ in scores if s == 2)
    n_total = len(scores)
    pct = int(n_good / n_total * 100) if n_total else 0

    return {
        "id": scenario["id"], "name": scenario["name"], "type": scenario.get("type",""),
        "scores": scores, "violations": violations,
        "n_good": n_good, "n_total": n_total, "pct": pct,
        "passed": pct >= 75 and len(violations) == 0,
    }


def run_isolation_tests(tokens):
    results = []
    for t in ISOLATION_TESTS:
        rid = t["restaurant"]
        token = tokens.get(rid)
        if not token:
            results.append({**t, "bot": "NO_TOKEN", "passed": False, "reason": "no token"})
            continue
        bot = simulate_thread([t["message"]], token)
        time.sleep(DELAY)

        # Check must_say_no
        no_phrases = ["ما عندنا", "مو عندنا", "مو في المنيو", "ما في منيو",
                      "ما يتوفر", "مو متوفر", "ما نتوصل", "الكرخ فقط",
                      "كاش فقط", "ما نقبل", "مو دوامنا"]
        passes = True
        reason = ""

        if t.get("must_say_no"):
            if not any(p in bot for p in no_phrases):
                passes = False
                reason = "لم يقل لا"
            for fp in t.get("forbidden_prices", []):
                if fp in bot:
                    passes = False
                    reason = f"ذكر سعر ممنوع: {fp}"

        for fc in t.get("forbidden_content", []):
            if fc in bot:
                passes = False
                reason = f"محتوى ممنوع: {fc}"

        results.append({**t, "bot": bot, "passed": passes, "reason": reason})
    return results


def run_dynamic_tests(tokens):
    results = []
    for t in DYNAMIC_TESTS:
        rid = t["restaurant"]
        token = tokens.get(rid)
        if not token:
            results.append({**t, "passed": False, "reason": "no token"})
            continue

        # Ask before update
        bot_before = simulate_thread([t["question"]], token)
        time.sleep(DELAY)

        # Get product id and update price
        pid = get_product_id(token, t["product_name"])
        if not pid:
            results.append({**t, "passed": False, "reason": "product not found"})
            continue
        update_product_price(token, pid, t["new_price"])
        time.sleep(1)  # wait for update

        # Ask after update
        bot_after = simulate_thread([t["question"]], token)
        time.sleep(DELAY)

        # Revert
        update_product_price(token, pid, t["old_price"])

        passed = (any(p in bot_after for p in t["must_contain_after"]) and
                  not any(p in bot_after for p in t["must_not_contain_after"]))
        results.append({
            **t,
            "bot_before": bot_before,
            "bot_after": bot_after,
            "passed": passed,
            "reason": "" if passed else "السعر القديم ما زال ظاهر",
        })
    return results


