def _req(method, path, data=None, token=None, timeout=30):
    headers = {"Content-Type": "application/json"}
    if token:
        headers["Authorization"] = f"Bearer {token}"
    body = json.dumps(data).encode() if data else None
    req  = urllib.request.Request(f"{BASE}{path}", data=body,
                                   headers=headers, method=method)
    try:
        with urllib.request.urlopen(req, timeout=timeout) as r:
            return json.loads(r.read()), r.status
    except urllib.error.HTTPError as e:
        raw = e.read()
        if not raw:
            return {}, e.code
        try:
            return json.loads(raw), e.code
        except Exception:
            return {}, e.code
    except Exception as exc:
        return {"error": str(exc)}, 0


def register_restaurant(cfg):
    body = {
        "email": cfg["email"], "password": cfg["password"],
        "restaurant_name": cfg["restaurant_name"], "owner_name": cfg["owner_name"],
        "plan": cfg.get("plan", "starter"),
    }
    resp, status = _req("POST", "/api/auth/register", body)
    if status == 400 and "مستخدم" in str(resp):
        return "already_exists"
    return "ok" if status == 200 else f"error:{status}"


def login_restaurant(cfg):
    resp, status = _req("POST", "/api/auth/login",
                         {"email": cfg["email"], "password": cfg["password"]})
    if status == 200:
        return resp.get("token")
    return None


def create_products(token, products):
    created = {}
    for p in products:
        resp, status = _req("POST", "/api/products",
                             {"name": p["name"], "price": p["price"],
                              "category": p["category"], "description": p.get("description",""),
                              "available": True, "variants": [], "gallery_images": []},
                             token=token)
        if status == 201:
            created[p["name"]] = resp.get("id")
        time.sleep(0.3)
    return created


def update_settings(token, settings):
    _req("PUT", "/api/settings", settings, token=token)


def get_product_id(token, product_name):
    resp, status = _req("GET", "/api/products", token=token)
    if status == 200:
        items = resp if isinstance(resp, list) else resp.get("items", [])
        for p in items:
            if p.get("name") == product_name:
                return p.get("id")
    return None


def update_product_price(token, pid, new_price):
    _req("PATCH", f"/api/products/{pid}", {"price": new_price}, token=token)


def simulate_thread(messages_list, token):
    data = json.dumps({"messages": messages_list, "scenario": "day8"}).encode()
    req  = urllib.request.Request(
        f"{BASE}/api/bot/simulate", data=data,
        headers={"Content-Type": "application/json",
                 "Authorization": f"Bearer {token}"},
        method="POST")
    for attempt in range(3):
        try:
            with urllib.request.urlopen(req, timeout=120) as r:
                body    = json.loads(r.read())
                results = body.get("results", [])
                return results[-1].get("bot", "") if results else ""
        except Exception as e:
            if attempt < 2:
                time.sleep(6)
            else:
                return f"ERROR:{e}"
    return "ERROR:max_retries"


