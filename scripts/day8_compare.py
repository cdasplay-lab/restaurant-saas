REGREETING = ["هلا بيك 🌷 شلون أكدر أخدمك؟", "أني مساعد مطعم",
              "مرحبًا بك", "أهلاً بك"]


def score_message(bot_reply, expected):
    is_meta = expected.startswith("META:")
    if "عذراً، حدث خطأ تقني" in bot_reply or bot_reply.startswith("ERROR"):
        return 0, "❌ خطأ تقني"
    if any(p in bot_reply for p in COLD_PHRASES):
        return 0, "❌ نبرة رسمية"
    bot_len = len(bot_reply)
    exp_len = len(expected)
    if is_meta:
        return (2, "✅") if bot_len <= 250 else (1, f"⚠️ طويل ({bot_len})")
    if "ثبت" in expected or "✅" in expected or "✅" in bot_reply:
        return (2, "✅") if bot_len <= 250 else (1, f"⚠️ ملخص طويل ({bot_len})")
    if exp_len <= 20:
        if bot_len <= 50:   return 2, "✅"
        if bot_len <= 100:  return 1, f"⚠️ طويل ({bot_len}>{exp_len})"
        return 0, f"❌ طويل جدًا ({bot_len}>{exp_len})"
    if bot_len > exp_len * 1.6:
        return 1, f"⚠️ طويل ({bot_len}>{exp_len})"
    return 2, "✅"


def check_coherence(scenario, user_msgs, bot_replies):
    checks  = scenario.get("checks", [])
    full    = " ".join(bot_replies)
    violations = []

    if "no_regreeting" in checks:
        for r in REGREETING:
            if any(r in b for b in bot_replies[1:]):
                violations.append("no_regreeting")
                break

    if "no_repeat_name_q" in checks:
        name_qs = ["شنو اسمك", "اسمك شنو", "كللي اسمك", "شو اسمك"]
        found = [i for i, b in enumerate(bot_replies) if any(q in b for q in name_qs)]
        if len(found) > 1:
            violations.append("no_repeat_name_q")

    if "no_early_confirm" in checks:
        for i, b in enumerate(bot_replies[:-1]):
            if "✅ طلبك" in b and i < len(bot_replies) - 2:
                violations.append("no_early_confirm")
                break

    if "single_summary" in checks:
        summ = [b for b in bot_replies if "✅ طلبك" in b or "ثبتت الطلب" in b]
        if len(summ) > 1:
            violations.append("single_summary")

    if "handoff_on_request" in checks:
        for i, m in enumerate(user_msgs):
            if "موظف" in m or "إنسان" in m or "ما أريد بوت" in m:
                if i < len(bot_replies):
                    b = bot_replies[i]
                    if not any(t in b for t in ["أحولك", "تحويل", "موظف"]):
                        violations.append("handoff_on_request")
                break

    if "no_upsell_during_complaint" in checks:
        complaint_started = False
        complaint_kws = ["متأخر", "غلط", "بارد", "ناقص", "مشكلة", "غير صحيح", "مو طلبي"]
        upsell_kws = ["تطلب", "شنو تحب", "المنيو", "شيء آخر", "إضافة"]
        for i, m in enumerate(user_msgs):
            if any(k in m for k in complaint_kws):
                complaint_started = True
            if complaint_started and i < len(bot_replies):
                if any(k in bot_replies[i] for k in upsell_kws):
                    violations.append("no_upsell_during_complaint")
                    break

    if "recalls_conversation_info" in checks:
        # يتحقق أن الرد على "شنو سجلت علي؟" يحتوي معلومات من المحادثة (≥15 حرف)
        for i, m in enumerate(user_msgs):
            if "سجلت" in m:  # فقط رسالة الاستفسار "شنو سجلت علي؟"، ليس "اسمي X"
                if i < len(bot_replies) and len(bot_replies[i]) < 15:
                    violations.append("recalls_conversation_info")
                break

    if "_summary_must_contain" in scenario:
        last_bot = bot_replies[-1] if bot_replies else ""
        for item in scenario["_summary_must_contain"]:
            norm_b = last_bot.replace("ج", "گ").replace("غ", "گ")
            norm_i = item.replace("ج", "گ").replace("غ", "گ")
            if norm_i not in norm_b:
                violations.append(f"summary_missing:{item}")

    return violations


