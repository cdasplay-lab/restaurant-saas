    total_assertions = total_data + total_msg
    good_assertions  = good_data  + good_msg
    pct_overall = int(good_assertions / total_assertions * 100) if total_assertions else 0

    icon = "✅" if pct_overall >= target_pct else ("⚠️" if pct_overall >= 80 else "❌")

