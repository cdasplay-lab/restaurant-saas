def save_report(summary):
    path = "scripts/day8_compare_report.txt"
    lines = [
        "Day 8 — Multi-Restaurant Launch Simulation",
        f"تاريخ: {datetime.datetime.now().strftime('%Y-%m-%d %H:%M')}",
        "=" * W, "",
    ]
    for rid, data in summary["restaurants"].items():
        lines.append(f"{rid} — {data['name']}: {data['pct']}% {'✅' if data['ready'] else '❌'}")
    lines.append("")
    iso = summary["isolation"]
    lines.append(f"Isolation: {iso['passed']}/{iso['total']} {'✅' if iso['passed']==iso['total'] else '❌'}")
    dyn = summary["dynamic"]
    lines.append(f"Dynamic:   {dyn['passed']}/{dyn['total']} {'✅' if dyn['passed']==dyn['total'] else '❌'}")
    lines.append("")
    lines.append(f"الإجمالي: {summary['overall_pct']}%  —  {summary['launch_ready']}")
