# ── Auth ──────────────────────────────────────────────────────────────────────
def get_token():
    data = json.dumps({"email": "admin@restaurant.com", "password": "admin123"}).encode()
    req = urllib.request.Request(f"{BASE}/api/auth/login", data=data, headers={"Content-Type": "application/json"}, method="POST")
    with urllib.request.urlopen(req, timeout=10) as r:
        return json.loads(r.read())["token"]

TOKEN = get_token()

def simulate(messages, scenario: str) -> str:
    """Send one or more messages to simulate endpoint, return LAST bot reply text.
    messages can be str (single) or list (multi-turn).
    """
    if isinstance(messages, str):
        messages = [messages]
    data = json.dumps({"messages": messages, "scenario": scenario}).encode()
    req = urllib.request.Request(
        f"{BASE}/api/bot/simulate", data=data,
        headers={"Content-Type": "application/json", "Authorization": f"Bearer {TOKEN}"},
        method="POST"
    )
    try:
        with urllib.request.urlopen(req, timeout=90) as r:
            body = json.loads(r.read())
            results = body.get("results", [])
            if results:
                return results[-1].get("bot", "")
            return ""
    except Exception as e:
        return f"ERROR:{e}"

