def run_test(t):
    tid, cat, q, keywords = t
    q_display = q if isinstance(q, str) else " → ".join(q)  # list = multi-turn
    reply = simulate(q, f"day1_q{tid}")
    if reply.startswith("ERROR:"):
        return (tid, cat, q_display, reply, keywords, "error")
    passed = any(kw in reply for kw in keywords)
    return (tid, cat, q_display, reply, keywords, "pass" if passed else "fail")

