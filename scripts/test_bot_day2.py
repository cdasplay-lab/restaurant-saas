import json, sys, time, urllib.request

BASE   = "http://localhost:8000"
DELAY  = 0.5   # seconds between calls

def get_token():
    data = json.dumps({"email":"admin@restaurant.com","password":"admin123"}).encode()
    req  = urllib.request.Request(f"{BASE}/api/auth/login", data=data,
                                   headers={"Content-Type":"application/json"}, method="POST")
    with urllib.request.urlopen(req, timeout=10) as r:
        return json.loads(r.read())["token"]

TOKEN = get_token()

def simulate(messages, scenario):
    if isinstance(messages, str):
        messages = [messages]
    data = json.dumps({"messages": messages, "scenario": scenario}).encode()
    req  = urllib.request.Request(
        f"{BASE}/api/bot/simulate", data=data,
        headers={"Content-Type":"application/json","Authorization":f"Bearer {TOKEN}"},
        method="POST")
    try:
        with urllib.request.urlopen(req, timeout=90) as r:
            body    = json.loads(r.read())
            results = body.get("results", [])
            return results[-1].get("bot","") if results else ""
    except Exception as e:
        return f"ERROR:{e}"

# ─── score helper ────────────────────────────────────────────────────────────
def score_reply(reply, keywords):
    if reply.startswith("ERROR"):
        return 0
    if not any(k in reply for k in keywords):
        return 0
    return 2 if len(reply) <= 160 else 1

