# ─── Runner ───────────────────────────────────────────────────────────────────
def run():
    scores   = {0:[], 1:[], 2:[]}
    by_cat   = {}
    results  = []

    print("\033[1m" + "═"*55)
    print("  Day 2 Bot Tests — 200 Questions")
    print("═"*55 + "\033[0m\n")

    for tid, cat, q, keywords in TESTS:
        q_display = q if isinstance(q, str) else " → ".join(q)
        reply = simulate(q, f"day2_q{tid}")
        s     = score_reply(reply, keywords)
        scores[s].append(tid)
        by_cat.setdefault(cat, []).append(s)
        results.append((tid, cat, q_display, reply, s))

        icon = "\033[32m2\033[0m" if s==2 else ("\033[33m1\033[0m" if s==1 else "\033[31m0\033[0m")
        preview = reply[:80].replace("\n"," ")
        print(f"  [{icon}] [{tid:>3}] {q_display[:45]:<45} → {preview}")
        time.sleep(DELAY)

    # ── Summary ───────────────────────────────────────────────────────────────
    total   = len(TESTS)
    passed  = len(scores[2]) + len(scores[1])
    score2  = len(scores[2])
    score1  = len(scores[1])
    score0  = len(scores[0])

    print("\n" + "\033[1m" + "═"*55)
    print(f"  RESULTS: {score2}/200 جاهز  {score1}/200 مقبول  {score0}/200 خطأ")
    print(f"  PASS rate: {passed}/{total} ({100*passed//total}%)")
    print("═"*55 + "\033[0m")

    if scores[0]:
        print("\n\033[1m\033[31mFAILURES (score=0):\033[0m")
        for tid, cat, q_display, reply, s in results:
            if s == 0:
                kws = next(kw for t,c,_q,_r,_s in [(x[0],x[1],x[2],x[3],x[4]) for x in results]
                           if t==tid for kw in [[]])
                print(f"  [{tid:>3}] [{cat}] {q_display}")
                print(f"        رد:  {reply[:120]}")

    print("\n\033[1mCategory Breakdown:\033[0m")
    for cat in ["طلب","تخصيص","تغيير","بيانات","تأكيد"]:
        ss = by_cat.get(cat,[])
        n2 = ss.count(2); n1 = ss.count(1); n0 = ss.count(0)
        bar2 = "█"*n2; bar1 = "░"*n1; bar0 = " "*n0
        col  = "\033[32m" if n0==0 else ("\033[33m" if n0<=5 else "\033[31m")
        print(f"  {col}{cat:<8}  {n2:>2}✓ {n1:>2}~ {n0:>2}✗  [{bar2}{bar1}]\033[0m")

    rate = 100*passed//total
    col  = "\033[32m" if rate>=90 else ("\033[33m" if rate>=70 else "\033[31m")
    print(f"\n{col}\033[1mScore: {rate}%\033[0m\n")

if __name__ == "__main__":
    run()
