# ── Runner ────────────────────────────────────────────────────────────────────
BOLD = "\033[1m"; RED = "\033[31m"; GRN = "\033[32m"
YLW = "\033[33m"; RST = "\033[0m"

def run():
    print(f"\n{BOLD}═══════════════════════════════════════════════════════")
    print(f"  Day 3 Story Reply Tests — 200 Questions")
    print(f"═══════════════════════════════════════════════════════{RST}\n")

    results = []
    section_scores = {}
    generic_count  = 0

    for (qid, section, story_fn, msg, keywords) in TESTS:
        full_msg = story_fn(msg)
        reply    = simulate(full_msg)
        score    = score_reply(reply, keywords)
        generic  = is_generic(reply, section)
        if generic: generic_count += 1

        # Display
        col   = GRN if score==2 else (YLW if score==1 else RED)
        gflag = " ⚠️GENERIC" if generic else ""
        short = reply[:70] + ("…" if len(reply)>70 else "")
        # Shorten display msg
        dmsg  = msg[:35] + ("…" if len(msg)>35 else "")
        print(f"  [{col}{score}{RST}] [{qid:3}] [{section}] {dmsg:<37} → {short}{gflag}")

        results.append((qid, section, score, generic))
        if section not in section_scores: section_scores[section] = []
        section_scores[section].append(score)
        time.sleep(DELAY)

    # ── Summary ──────────────────────────────────────────────────────────────
    total      = len(results)
    passed     = sum(1 for _,_,s,_ in results if s > 0)
    score2     = sum(1 for _,_,s,_ in results if s == 2)
    score1     = sum(1 for _,_,s,_ in results if s == 1)
    failed     = sum(1 for _,_,s,_ in results if s == 0)

    print(f"\n{BOLD}═══════════════════════════════════════════════════════")
    print(f"  RESULTS: {score2}/200 جاهز  {score1}/200 مقبول  {failed}/200 خطأ")
    print(f"  PASS rate: {passed}/200 ({passed*100//total}%)")
    if generic_count:
        print(f"  ⚠️  Generic (no story context): {generic_count} replies")
    print(f"═══════════════════════════════════════════════════════{RST}\n")

    failures = [(qid, sec, r) for (qid, sec, s, _) in results for r in [results[qid-1]] if s == 0]
    # Rebuild failures properly
    fail_list = [(qid, sec) for (qid, sec, s, _) in results if s == 0]
    if fail_list:
        print(f"{BOLD}{RED}FAILURES (score=0):{RST}")
        for (qid, sec) in fail_list:
            # Get the original test
            t = TESTS[qid-1]
            full_msg = t[2](t[3])
            # Re-simulate to show reply
            reply = simulate(full_msg)
            short = reply[:80] + ("…" if len(reply)>80 else "")
            print(f"  [{qid:3}] [{sec}] {t[3]}")
            print(f"        رد:  {short}")

    # ── Category breakdown ────────────────────────────────────────────────────
    SECTION_ORDER = ["منتج","عرض","منيو","فيديو","عامة","غامض"]
    SECTION_TOTAL = {"منتج":40,"عرض":40,"منيو":30,"فيديو":30,"عامة":30,"غامض":30}

    print(f"\n{BOLD}Category Breakdown:{RST}")
    for sec in SECTION_ORDER:
        if sec not in section_scores: continue
        scores  = section_scores[sec]
        ok      = sum(1 for s in scores if s > 0)
        s2      = sum(1 for s in scores if s == 2)
        s1      = sum(1 for s in scores if s == 1)
        bad     = sum(1 for s in scores if s == 0)
        total_s = SECTION_TOTAL[sec]
        bar_w   = int(ok / total_s * 40)
        bar     = "█"*bar_w + "░"*(40-bar_w)
        col     = GRN if bad == 0 else (YLW if bad <= 2 else RED)
        print(f"  {col}{sec:<6}  {ok:2}✓  {bad:2}✗  [{bar}]{RST}")

    pct = passed*100//total
    col = GRN if pct >= 97 else (YLW if pct >= 90 else RED)
    print(f"\n{col}{BOLD}Score: {pct}%{RST}\n")

if __name__ == "__main__":
    run()
