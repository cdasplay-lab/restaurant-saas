def main():
    print(f"\n⏳ جاري تشغيل 120 سؤال... (قد يستغرق ~4 دقائق)")

    token = get_token()

    # Build section-5 thread incrementally
    section5_msgs = []

    results = []
    passed = failed = 0

    for i, (qid, section, question, expected) in enumerate(CASES):
        is_section5 = section == "ثبات السلوك"

        if is_section5:
            # Cumulative thread — keep last 20 (API limit)
            section5_msgs.append(question)
            window = section5_msgs[-20:]  # API max is 20 messages
            bot_reply = simulate_thread(window, token)
        else:
            bot_reply = simulate(question, token)

        # Retry once on transient server errors
        if bot_reply.startswith("ERROR") or "عذراً، حدث خطأ تقني" in bot_reply:
            time.sleep(5)
            if is_section5:
                bot_reply = simulate_thread(window, token)
            else:
                bot_reply = simulate(question, token)

        score, note, winner = score_reply(qid, section, bot_reply, expected)

        ok = score >= 2
        if ok:
            passed += 1
            sym = "✅"
        else:
            failed += 1
            sym = "❌"

        print(f"  {sym} [{qid}] {section} — {question[:40]}")
        results.append((qid, section, question, bot_reply, expected, score, note, winner))

        time.sleep(DELAY)

    total = len(CASES)
    pct   = int(passed / total * 100)

    bot_wins = sum(1 for r in results if r[7] == "البوت")
    exp_wins = sum(1 for r in results if r[7] == "الجواب المتوقع")
    ties     = sum(1 for r in results if r[7] == "متقارب")

    print(f"\n{'='*60}")
    print(f"  النتيجة: {passed}/{total} ({pct}%)")
    print(f"  جاهز للإطلاق (2): {passed}  |  مقبول (1): 0  |  خطأ (0): {failed}")
    print(f"  البوت أفضل: {bot_wins}  |  الجواب المتوقع أفضل: {exp_wins}  |  متقارب: {ties}")

    # Save report
    report_path = "scripts/day6_compare_report.txt"
    lines = [f"Day 6 — تقرير المقارنة | {datetime.datetime.now():%Y-%m-%d %H:%M}",
             f"النتيجة: {passed}/{total} ({pct}%)",
             ""]

    # Section breakdown
    sections_order = ["الاسم", "التفضيلات", "memory", "corrections", "ثبات السلوك"]
    for sec in sections_order:
        sec_results = [r for r in results if r[1] == sec]
        sec_passed  = sum(1 for r in sec_results if r[5] >= 2)
        sec_total   = len(sec_results)
        sec_pct     = int(sec_passed / sec_total * 100) if sec_total else 0
        lines.append(f"  {sec}: {sec_passed}/{sec_total} ({sec_pct}%)")

    lines.append("")
    lines.append("الحالات الفاشلة:")
    for r in results:
        qid, section, question, bot_reply, expected, score, note, winner = r
        if score < 2:
            lines.append(f"\n  [{qid}] {section} — {question}")
            lines.append(f"       البوت: {bot_reply[:120]}")
            lines.append(f"       {note}")

    lines.append(analyze_patterns(results))

    with open(report_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))

    print(f"\n  ✅ التقرير محفوظ في: {report_path}")
    print(f"{'='*60}\n")

