def get_token():
    data = json.dumps({"email": "admin@restaurant.com", "password": "admin123"}).encode()
    req  = urllib.request.Request(
        f"{BASE}/api/auth/login", data=data,
        headers={"Content-Type": "application/json"}, method="POST")
    with urllib.request.urlopen(req, timeout=10) as r:
        return json.loads(r.read())["token"]


def simulate(msg, token):
    """Send single message, return bot reply."""
    data = json.dumps({"messages": [msg], "scenario": "default"}).encode()
    req  = urllib.request.Request(
        f"{BASE}/api/bot/simulate", data=data,
        headers={"Content-Type": "application/json",
                 "Authorization": f"Bearer {token}"},
        method="POST")
    try:
        with urllib.request.urlopen(req, timeout=90) as r:
            body    = json.loads(r.read())
            results = body.get("results", [])
            return results[-1].get("bot", "") if results else ""
    except Exception as e:
        return f"ERROR:{e}"


def simulate_thread(messages_list, token):
    """Send cumulative thread, return bot reply for last message."""
    data = json.dumps({"messages": messages_list, "scenario": "default"}).encode()
    req  = urllib.request.Request(
        f"{BASE}/api/bot/simulate", data=data,
        headers={"Content-Type": "application/json",
                 "Authorization": f"Bearer {token}"},
        method="POST")
    try:
        with urllib.request.urlopen(req, timeout=90) as r:
            body    = json.loads(r.read())
            results = body.get("results", [])
            return results[-1].get("bot", "") if results else ""
    except Exception as e:
        return f"ERROR:{e}"


# ── Strict Scoring ────────────────────────────────────────────────────────────

def score_reply(qid, section, bot_reply, expected):
    """
    Day 6 — صارم جدًا
    2 = مستوى الجواب المتوقع أو أفضل
    1 = مقبول لكن الجواب المتوقع أفضل
    0 = خطأ
    """
    if bot_reply.startswith("ERROR"):
        return 0, "خطأ تقني", "الجواب المتوقع"

    # ── فشل: hallucination — ادّعى وجود memory غير موجودة ─────────────────
    hallucination_cases = {
        "Q10", "Q11",                           # لا يعرف الاسم
        "Q51", "Q53", "Q54", "Q67",             # لا يعرف آخر طلب/منتج مفضل
        "Q39",                                   # لا يعرف التفضيلات
        "Q55",                                   # لا يعرف العنوان السابق
        "Q59",                                   # لا يعرف التفضيلات
    }
    if qid in hallucination_cases:
        # الجواب المتوقع = "ما عندي ..." — إذا البوت ادّعى معرفة شي → فشل
        # "اسمك" alone is too broad (appears in "ما عندي اسمك محفوظ" which is correct)
        # Only flag when bot claims a specific stored value
        hallucination_signs = [
            "اسمك محمد", "اسمك علي", "اسمك هو", "المسجل عندي:",
            "آخر طلب كان", "أكثر شي تطلبه هو", "آخر مرة أخذت",
            "تفضيلاتك الحالية", "تفضيلاتك: ",
        ]
        if any(s in bot_reply for s in hallucination_signs):
            return 0, "❌ هلوسة — ادّعى memory غير موجودة", "الجواب المتوقع"

    # ── فشل: نبرة باردة/رسمية ────────────────────────────────────────────────
    if any(p in bot_reply for p in COLD_PHRASES):
        return 0, "❌ نبرة رسمية باردة", "الجواب المتوقع"

    # ── فشل: أعاد التحية في وسط thread ────────────────────────────────────
    if section == "ثبات السلوك" and qid != "Q96":
        if any(g in bot_reply for g in REGREETING):
            return 0, "❌ أعاد الترحيب في وسط الجلسة", "الجواب المتوقع"

    # ── فشل: corrections — ما أقر بالتصحيح ──────────────────────────────────
    # Q10/Q11 هي memory queries (ما عندي اسم محفوظ) — مو تصحيح، لا تطبق عليها
    _MEMORY_QUERY_IDS = {"Q10", "Q11"}
    if section in ("corrections", "الاسم") and qid not in _MEMORY_QUERY_IDS:
        ack_kws = ["أكيد", "وصلت", "تمام", "تم", "وصلني", "أعتمد",
                   "ألتزم", "أشيل", "أخلي", "ما أستخدم", "ما أكرر",
                   "أبتعد", "حقك", "أغير", "أستخدم", "ما عندي",
                   "أعتذر", "آسف"]
        if not any(k in bot_reply for k in ack_kws):
            return 0, "❌ ما أقر بالتصحيح", "الجواب المتوقع"

    # ── فشل: memory section — رد عشوائي ──────────────────────────────────────
    if section == "memory":
        memory_kws = ["أكيد", "تمام", "تم", "وصلت", "وصلني", "ما عندي",
                      "ذكّرني", "أرسللي", "محفوظ", "اقتراح", "مرجع",
                      "أخمن", "بصراحة", "سجل", "قديم", "آسف", "آسفين",
                      "ما أعتمده", "ما أقدر", "حقك", "ألتزم", "أعتمده"]
        if not any(k in bot_reply for k in memory_kws):
            return 0, "❌ رد عشوائي لا علاقة له بالذاكرة", "الجواب المتوقع"

    # ── فشل: ثبات السلوك — تجاهل التعديل أو التراكم ─────────────────────────
    if section == "ثبات السلوك":
        context_kws = ["أكيد", "تمام", "تم", "وصلني", "وصلت", "العفو",
                       "بركر", "زينگر", "بدون", "محمد", "المنصور", "كاش",
                       "سجلت", "المسجل", "بدلنا", "ما أكرر", "ما أعيد",
                       "ثبتت", "رقم", "أكمل", "أكمللي", "الاسم", "الجديد",
                       "ثلج", "هلا", "شلون", "تسلم", "العفو", "آسف", "حقك",
                       "ما أنسى", "أكمل من", "ألتزم", "ما أعيده", "ما أعيد",
                       "سجل", "بركر", "زينگر"]
        if not any(k in bot_reply for k in context_kws):
            return 0, "❌ رد غير مناسب للسياق", "الجواب المتوقع"

    # ── الرد صحيح — نقيّم الجودة بصرامة ────────────────────────────────────
    score  = _quality_score(bot_reply, expected, section)
    winner = _winner(bot_reply, expected, score, section)
    note   = _note(score, bot_reply, expected, section)
    return score, note, winner


def _quality_score(bot_reply, expected, section):
    """2 = جيد، 1 = مقبول لكن يمكن أفضل"""
    bot_len = len(bot_reply)
    exp_len = len(expected)

    # نبرة رسمية/باردة
    if any(p in bot_reply for p in COLD_PHRASES):
        return 1

    # الاسم والتصحيحات: ردود قصيرة مطلوبة
    if section in ("الاسم", "corrections"):
        return 2 if bot_len <= 80 else 1

    # ثبات السلوك: السماح بـ receipt الطلب (أطول) إذا المحتوى صحيح
    if section == "ثبات السلوك":
        return 2 if bot_len <= 200 else 1

    # التفضيلات والذاكرة: إذا البوت أطول بـ 50%+ → مقبول لكن الجواب المتوقع أفضل
    if exp_len > 20 and bot_len > exp_len * 1.5:
        return 1

    return 2


def _winner(bot_reply, expected, score, section):
    if score == 0:
        return "الجواب المتوقع"

    bot_len = len(bot_reply)
    exp_len = len(expected)

    # نبرة باردة رسمية → الجواب المتوقع أفضل
    if any(p in bot_reply for p in COLD_PHRASES):
        return "الجواب المتوقع"

    # أعاد الترحيب → الجواب المتوقع أفضل
    if any(g in bot_reply for g in REGREETING):
        return "الجواب المتوقع"

    # الاسم/تصحيحات: البوت أطول من المتوقع بـ 15%+ → الجواب المتوقع أفضل
    if section in ("الاسم", "corrections") and exp_len > 10 and bot_len > exp_len * 1.15:
        return "الجواب المتوقع"

    # ثبات السلوك: السماح بـ receipt format، فقط إذا أطول بـ 100%+
    if section == "ثبات السلوك" and exp_len > 10 and bot_len > exp_len * 2.0:
        return "الجواب المتوقع"

    # أي قسم: البوت أطول بـ 30%+ → الجواب المتوقع أفضل
    if exp_len > 20 and bot_len > exp_len * 1.3:
        return "الجواب المتوقع"

    # البوت أقصر بشكل ملحوظ → البوت أفضل
    if bot_len <= exp_len * 0.85:
        return "البوت"

    return "متقارب"


def _note(score, bot_reply, expected, section):
    if score == 0:
        return "❌ فشل"
    if score == 1:
        bot_len, exp_len = len(bot_reply), len(expected)
        if any(p in bot_reply for p in COLD_PHRASES):
            return "⚠️ نبرة باردة"
        if section in ("الاسم", "corrections", "ثبات السلوك") and bot_len > 80:
            return "⚠️ طويل للقسم"
        if exp_len > 20 and bot_len > exp_len * 1.5:
            return f"⚠️ طويل ({bot_len}>{exp_len})"
        return "⚠️ مقبول لكن يمكن أفضل"
    return "✅ جاهز"


# ── Analysis ──────────────────────────────────────────────────────────────────

def analyze_patterns(results):
    exp_better = [r for r in results if r[7] == "الجواب المتوقع"]
    exp_better_sorted = sorted(exp_better,
                               key=lambda r: len(r[3]) - len(r[4]),
                               reverse=True)

    lines = []
    lines.append("\n" + "="*70)
    lines.append("📊 تحليل Day 6 — أين الجواب المتوقع أفضل؟")
    lines.append("="*70)

    lines.append(f"\n🔴 أكثر 20 حالة الجواب المتوقع كان أوضح:\n")
    top20 = exp_better_sorted[:20]
    for i, r in enumerate(top20, 1):
        qid, section, question, bot_reply, expected, score, note, _ = r
        lines.append(f"  {i:2}. [{qid}] {section} — {question[:35]}")
        lines.append(f"       البوت     : {bot_reply[:80]}")
        lines.append(f"       المتوقع   : {expected[:80]}")
        lines.append(f"       الفرق     : {note}\n")

    lines.append("\n" + "─"*70)
    lines.append("🔍 أبرز الأنماط لازم البوت يقلدها:\n")

    patterns = {
        "تأكيد فوري بدون شرح زيادة (وصلت/أكيد/تمام)": 0,
        "يقول صراحة ما عندي سجل عند غياب memory": 0,
        "لا يكرر اسم العميل بدون داعي": 0,
        "يطبق التصحيح فورًا": 0,
        "لا يعود للغلط القديم": 0,
        "ردود ≤ 50 حرف في الاسم والتصحيحات": 0,
        "لا يبدأ بـ (هلا بيك) في وسط الجلسة": 0,
    }
    for r in exp_better:
        bot_reply = r[3]
        expected  = r[4]
        if len(bot_reply) > len(expected):
            patterns["تأكيد فوري بدون شرح زيادة (وصلت/أكيد/تمام)"] += 1
        if "ما عندي" in expected and "ما عندي" not in bot_reply:
            patterns["يقول صراحة ما عندي سجل عند غياب memory"] += 1
        if any(g in bot_reply for g in REGREETING):
            patterns["لا يبدأ بـ (هلا بيك) في وسط الجلسة"] += 1

    for p, c in sorted(patterns.items(), key=lambda x: -x[1]):
        if c > 0:
            lines.append(f"  • {p}: {c} حالة")

    lines.append("\n" + "─"*70)
    lines.append("💡 التوصيات:\n")
    lines.append("  1. أقصر ردود في الاسم والتصحيحات (جملة واحدة)")
    lines.append("  2. عند غياب memory: قل 'ما عندي ...' مباشرة بدون شرح")
    lines.append("  3. طبّق كل تصحيح فوراً بدون تكرار الغلط")
    lines.append("  4. لا ترجع للترحيب في وسط الجلسة")

    return "\n".join(lines)


